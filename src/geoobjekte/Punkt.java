package geoobjekte;

public class Punkt {

    // Attribute
    private int x ;
    private int y ;

    // Konstruktor
    public Punkt( int x , int y ) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return this.x;
    }

    public int getY() {
        return this.y;
    }
}
